    public boolean isvalid() {
        String ma = txtMaNH.getText();
        NguoiHoc model = dao.findById(ma);
        if (txtMaNH.getText().length() == 0) {
            DialogHelper.alert(this, "Không để trống mã!");
            txtMaNH.requestFocus();
            return false;
        } else if (txtMaNH.getText().length() != 7) {
            DialogHelper.alert(this, "Phải đúng 7 kí tự!");
            txtMaNH.requestFocus();
            return false;
        } else if (model != null) {
            DialogHelper.alert(this, "Mã không được trùng");
            txtMaNH.requestFocus();
            return false;

        } else if (!txtHoTen.getText().matches("[a-zA-Z  ]+")) {
            DialogHelper.alert(this, "Sai định dạng họ tên");
            txtHoTen.requestFocus();
            return false;
        } else if (!txtDiaChi.getText().matches("\\w+@\\w+(\\.\\w+){1,2}")) {
            DialogHelper.alert(this, "Không đúng dạng email");
            txtDiaChi.requestFocus();
            return false;
        }

        return true;
    }

    public boolean checkNgaySinh() {

        Date NgaySinh = DateHelper.toDate(txtNgaySinh.getText(), "dd/MM/yyyy");

        Date NgayHienTai = new Date();
        Calendar now = Calendar.getInstance();
        Calendar cNgaySinh = Calendar.getInstance();
        int tuoi = NgayHienTai.getYear() - NgaySinh.getYear();
        if (tuoi >= 16) {
            return true;
        } else {
            DialogHelper.alert(this, "Trước ngày hiện tạo 16 năm!");
            return false;
        }

    }

    void load() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtTimKiem.getText();
            List<NguoiHoc> list = dao.selectByKeyword(keyword);
            for (NguoiHoc nh : list) {
                Object[] row = {
                    nh.getMaNH(),
                    nh.getHoTen(),
                    nh.isGioiTinh() ? "Nam" : "Nữ",
                    DateHelper.toString(nh.getNgaySinh()),
                    nh.getDienThoai(),
                    nh.getEmail(),
                    nh.getMaNV(),
                    DateHelper.toString(nh.getNgayDK())
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void insert() {
        if (isvalid() && checkNgaySinh()) {
            NguoiHoc model = getModel();
            try {
                dao.insert(model);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Thêm mới thất bại!" + e);
            }
        }
    }

    void update() {
        if (checkNgaySinh() && isvalid()) {
            NguoiHoc model = getModel();
            try {
                dao.update(model);
                this.load();
                DialogHelper.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Cập nhật thất bại!");
            }
        }
    }

    void delete() {
        if (DialogHelper.confirm(this, "Bạn thực sự muốn xóa người học này?")) {
            String manh = txtMaNH.getText();
            try {
                dao.delete(manh);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Xóa thành công!");
            } catch (HeadlessException e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
    }

    void clear() {

        NguoiHoc model = new NguoiHoc();
        model.setMaNV(ShareHelper.USER.getMaNV());
        model.setNgayDK(DateHelper.now());
        this.setModel(model);

    }

    void edit() {
        try {
            String manh = (String) tblGridView.getValueAt(this.index, 0);
            NguoiHoc model = dao.findById(manh);
            if (model != null) {
                this.setModel(model);
                this.setStatus(false);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void setModel(NguoiHoc model
    ) {
        txtMaNH.setText(model.getMaNH());
        txtHoTen.setText(model.getHoTen());
        cboGioitinh.setSelectedIndex(model.isGioiTinh() ? 0 : 1);
        txtNgaySinh.setText(DateHelper.toString(model.getNgaySinh()));
        txtDienThoai.setText(model.getDienThoai());
        txtDiaChi.setText(model.getEmail());
        txtGhiChu.setText(model.getGhiChu());
    }

    NguoiHoc getModel() {
        NguoiHoc model = new NguoiHoc();
        model.setMaNH(txtMaNH.getText());
        model.setHoTen(txtHoTen.getText());
        model.setGioiTinh(cboGioitinh.getSelectedIndex() == 0);
        model.setNgaySinh(DateHelper.toDate(txtNgaySinh.getText()));
        model.setDienThoai(txtDienThoai.getText());
        model.setEmail(txtDiaChi.getText());
        model.setGhiChu(txtGhiChu.getText());
        model.setMaNV(ShareHelper.USER.getMaNV());
        model.setNgayDK(DateHelper.now());
        return model;
    }

    void setStatus(boolean insertable
    ) {
        txtMaNH.setEditable(insertable);
        btnThem.setEnabled(insertable);
        btnSua.setEnabled(!insertable);
        btnXoa.setEnabled(!insertable);

        boolean first = this.index > 0;
        boolean last = this.index < tblGridView.getRowCount() - 1;
        btnFirst.setEnabled(!insertable && first);
        btnPrev.setEnabled(!insertable && first);
        btnLast.setEnabled(!insertable && last);
        btnNext.setEnabled(!insertable && last);
    }

